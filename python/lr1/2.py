# Знайти методом підбору корінь рівняння 4*х+5=25. Пошук почати з х=1, збільшувати кожне наступне значення х на 1

x = 1

while 4 * x + 5 != 25:
	print("При значенні х = %d рівняння сенсу немає" % x)
	x += 1

print("x = %d" % x)
